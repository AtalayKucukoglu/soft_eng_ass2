public class Values {
    private int base;
    private int exp;

    public Values(int base, int exp) {
        this.base = base;
        this.exp = exp;
    }

    public Values() {
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }
}
